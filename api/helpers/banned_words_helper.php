/**
 * Check if a name contains any banned words
 * 
 * @param string $name The name to check
 * @return array ['contains_banned' => bool, 'matched_words' => array]
 */
function checkBannedWords($name) {
    if (empty($name) || !is_string($name)) {
        return [
            'contains_banned' => false,
            'matched_words' => []
        ];
    }
    
    $bannedWords = getBannedWords();
    if (empty($bannedWords)) {
        return [
            'contains_banned' => false,
            'matched_words' => []
        ];
    }
    
    // Convert name to lowercase for case-insensitive matching
    $nameLower = mb_strtolower($name, 'UTF-8');
    
    // Remove common punctuation and spaces for more thorough checking
    $nameClean = preg_replace('/[^a-z0-9]/', '', $nameLower);
    
    $matchedWords = [];
    
    foreach ($bannedWords as $bannedWord) {
        $bannedLower = mb_strtolower(trim($bannedWord), 'UTF-8');
        if (empty($bannedLower)) {
            continue;
        }
        
        // Check if banned word appears in the name (case-insensitive)
        // Check both the original name and cleaned version
        if (mb_strpos($nameLower, $bannedLower) !== false || 
            mb_strpos($nameClean, preg_replace('/[^a-z0-9]/', '', $bannedLower)) !== false) {
            $matchedWords[] = $bannedWord;
        }
    }
    
    return [
        'contains_banned' => !empty($matchedWords),
        'matched_words' => $matchedWords
    ];
}
